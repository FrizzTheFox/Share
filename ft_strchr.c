#include <stdio.h>
#include <string.h>

char	*ft_strchr(char *s, int c)
{
	while (*s != '\0')
	{
		if (*s == c)
			return (s);
		s++;
	}
	s++;
	if (*s == c)
		return (s);
	else
		return (NULL);
}
